package com.bean;

public class Provider {
	private int providerId;
	private String providerName;
	private String providerAdd;
	private int providerTel;
	private String account;
	private String email;

	public int getProviderId() {
		return providerId;
	}
	public void setProviderId(int providerId) {
		this.providerId = providerId;
	}
	public String getProviderName() {
		return providerName;
	}
	public void setProviderName(String providerName) {
		this.providerName = providerName;
	}
	public String getProviderAdd() {
		return providerAdd;
	}
	public void setProviderAdd(String providerAdd) {
		this.providerAdd = providerAdd;
	}
	public int getProviderTel() {
		return providerTel;
	}
	public void setProviderTel(int providerTel) {
		this.providerTel = providerTel;
	}
	public String getAccount() {
		return account;
	}
	public void setAccount(String account) {
		this.account = account;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	
}
